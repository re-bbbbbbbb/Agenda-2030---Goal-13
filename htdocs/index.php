                <p id="listaparagrafoobiettivo13-1"><b>•</b>  Gli oceani si sono riscaldati, la neve e il ghiaccio sono diminuiti e il livello del mare si è alzato. Dal 1901 al 2010, il livello globale medio dei mari si è alzato di 19 cm, dato che gli oceani si sono espansi a causa del riscaldamento globale e dello scioglimento dei ghiacci;</p>
                <p id="listaparagrafoobiettivo13-1"><b>•</b>  Date le attuali concentrazioni e le continue emissioni di gas serra, è molto probabile che entro la fine di questo secolo, l’aumento della temperatura globale supererà 1,5°C rispetto al periodo dal 1850 al 1990. Gli oceani si riscalderanno e i ghiacci continueranno a sciogliersi. Si prevede che l’aumento medio del livello del mare raggiunga i 24-30 cm entro il 2065 e i 40-63 cm entro il 2100;</p>
