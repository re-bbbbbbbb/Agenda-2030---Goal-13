            Il cambiamento climatico interessa tutti i paesi. Sta sconvolgendo le <b>economie nazionali</b>, con <b>costi alti</b> per persone, comunità e paesi, che saranno più gravi un domani. <br/>
            Le persone stanno sperimentando gli <b>impatti significativi</b> del cambiamento climatico. Le emissioni di <b>gas a effetto serra</b>, derivanti dalle attività umane, sono la <b>forza trainante</b> del cambiamento climatico e continuano ad aumentare. Se non si prendono provvedimenti, si prevede che la <b>temperatura media</b> della superficie terrestre probabilmente aumenterà di <b>3°C</b>, invece alcune aree del pianeta sono destinate a un riscaldamento climatico ancora maggiore. <br/>
            Attualmente ci sono <b>soluzioni accessibili e flessibili</b> per permettere ai paesi di diventare economie più pulite e resistenti.  <br/>
            Il <b>ritmo del cambiamento</b> sta accelerando dato che sempre più persone utilizzano energie rinnovabili e mettono in pratica tutta una serie di misure che riducono le emissioni e aumentano gli sforzi di adattamento. <br/>
            Il cambiamento climatico è una <b>sfida globale</b> che non rispetta i confini nazionali. Le emissioni sono ovunque e riguardano tutti. È una questione che richiede <b>soluzioni coordinate</b> a livello internazionale e <b>cooperazione</b> al fine di aiutare i Paesi in via di sviluppo a muoversi verso un’economia a bassa emissione di carbonio.
            
